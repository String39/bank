package com.curso.modulo4.Bolea001_javaSE;

public class EjemploSwitch {

	public static void main(String[] args) {
	
		String a = "7";
		
		switch (a) {
	
		case "1":
			System.out.println("uno");
			break;
			
		case "2":
			System.out.println("dos");
			break;
			
		default:
			System.out.println("error");
			break;
		}
	}

}
